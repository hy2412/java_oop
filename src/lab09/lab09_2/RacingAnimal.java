package lab09.lab09_2;

public class RacingAnimal {
    private String name;
    private int speed;
    private int maxSpeed;

    public String getName() {
        return name;
    }
    public int getSpeed() {
        return speed;
    }
    public int getMaxSpeed() {
        return maxSpeed;
    }
    public void setSpeed(int speed) {
        this.speed = speed;
    }
}
